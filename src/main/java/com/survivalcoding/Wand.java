package com.survivalcoding;

public class Wand {
    private String name;
    private double power;

    public double getPower()
    {
        this.power = power;
        return power;
    }
    public void setPower(double power)
    {
        this.power = power;
    }

    public String getName()
    {
        return name;
    }
    public void setName(String name)
    {
        this.name = name;
    }
}
