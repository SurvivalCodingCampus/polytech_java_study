package com.survivalcoding;

public class Wand {
    private String name; // 지팡이이름
    private double power;  // 지팡이마력

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public double getPower() {
        return power;
    }

    public void setPower(double power) {
        this.power = power;
    }
}
